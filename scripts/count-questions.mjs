/**
 * Counts \questionitem occurrences in every QBT question .tex file and writes
 * src/data/questionCounts.generated.ts, keyed by the /tex/... PDF path used
 * in resources.ts. Run automatically via npm lifecycle hooks before build/dev/check.
 */

import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { join, resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');
const publicDir = join(repoRoot, 'public');

/** Recursively finds all _QBT__*.tex files (excluding solutions). */
function findQbtTexFiles(dir) {
  const results = [];
  let entries;
  try {
    entries = readdirSync(dir, { withFileTypes: true });
  } catch {
    return results;
  }
  for (const entry of entries) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) {
      results.push(...findQbtTexFiles(full));
    } else if (
      entry.name.startsWith('_QBT__') &&
      !entry.name.startsWith('_QBT___Solns') &&
      entry.name.endsWith('.tex')
    ) {
      results.push(full);
    }
  }
  return results;
}

const texFiles = findQbtTexFiles(join(publicDir, 'tex'));
const counts = {};

for (const texPath of texFiles) {
  const content = readFileSync(texPath, 'utf-8');
  // Count lines containing \questionitem, excluding the \newcommand definition line.
  const count = content
    .split('\n')
    .filter((line) => /\\questionitem/.test(line) && !/\\(?:new|renew)command/.test(line))
    .length;

  // Convert absolute fs path → /tex/... URL path with .pdf extension
  const rel = texPath.slice(publicDir.length).replace(/\\/g, '/');
  const filePath = rel.replace(/\.tex$/, '.pdf');
  counts[filePath] = count;
}

const entries = Object.entries(counts).sort(([a], [b]) => a.localeCompare(b));

const lines = [
  '// AUTO-GENERATED by scripts/count-questions.mjs — do not edit manually.',
  '// Regenerated on every `npm run build`, `npm run dev`, and `npm run check`.',
  'export const questionCounts: Record<string, number> = {',
  ...entries.map(([path, count]) => `  '${path}': ${count},`),
  '};',
  '',
];

const outPath = join(repoRoot, 'src', 'data', 'questionCounts.generated.ts');
writeFileSync(outPath, lines.join('\n'), 'utf-8');
console.log(`[count-questions] ${entries.length} entries written to src/data/questionCounts.generated.ts`);
